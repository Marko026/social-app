import { ThemeProvider } from '../context/ThemeProvider';
import SessionProvider from '../context/SessionProvider';

import type { Metadata } from 'next';
import { IBM_Plex_Sans, Inter } from 'next/font/google';
import { cookies } from 'next/headers';
import { getServerSession } from 'next-auth';
import { ReactQueryDevtools } from '@tanstack/react-query-devtools';

import { authOptions } from '@/lib/auth-options';
import QueryProvider from '@/context/QueryProvider';

import './globals.css';

// ----------------------------------------------------------------

const ibmPlexSans = IBM_Plex_Sans({
  weight: ['100', '200', '300', '400', '500', '600', '700'],
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await getServerSession(authOptions);
  const cookiesProvider = cookies();
  const theme = cookiesProvider.get('theme')?.value || 'dark';

  return (
    <ThemeProvider theme={theme}>
      <SessionProvider session={session}>
        <QueryProvider>
          <html lang="en" className={`${theme}`}>
            <body
              className={`${ibmPlexSans.className} min-h-screen bg-white-200 dark:bg-black-900`}
            >
              <main className="mx-auto max-w-screen-xxl">{children}</main>
              <ReactQueryDevtools />
            </body>
          </html>
        </QueryProvider>
      </SessionProvider>
    </ThemeProvider>
  );
}
